class Calc
{
     public static int add(int p, int q)
     {
          int result = p + q;
          return result;
     }
     public static int sub(int p, int q)
     {
          int result = p - q;
          return result;
     }
     public static int mul(int p, int q)
     {
          return p * q;
     }
     public static int div(int p, int q)
     {
          return p / q;
     }
}